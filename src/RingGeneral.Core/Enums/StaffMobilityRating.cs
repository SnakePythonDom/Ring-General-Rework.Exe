namespace RingGeneral.Core.Enums;

/// <summary>
/// Évaluation de la mobilité et volonté d'un staff à être partagé
/// </summary>
public enum StaffMobilityRating
{
    /// <summary>
    /// Faible mobilité - Préfère rester dans son environnement habituel
    /// </summary>
    Low,

    /// <summary>
    /// Mobilité moyenne - Accepte le partage selon les circonstances
    /// </summary>
    Medium,

    /// <summary>
    /// Haute mobilité - Très flexible, prêt à voyager et s'adapter
    /// </summary>
    High
}